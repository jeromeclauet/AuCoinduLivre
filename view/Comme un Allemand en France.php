            <h2>Comme un Allemand en France</h2>
            <br>Editer aux éditions de l’Iconoclaste, et écrit par Aurélie Luneau, Jeanne Guérout et Stefan Martens, ce livre retrace les quatre années d’Occupation vu par les Occupants.
            De nos yeux de contemporains, le régime nazi est un régime totalitaire et prônant une vision de l’être humain complètement fausse ! C’est que la plupart des historiens diront aujourd’hui sur le parti d’Adolf Hitler, à raison.
            Mais avec ce résumé, nous allons nous plonger dans les lettres des hommes/soldats allemands durant l’Occupation de la France de 1940 à 1944.
            Le travail effectué afin de récupérer ces morceaux importants de notre civilisation, permet d’ouvrir les yeux sur ce que composé exactement l’armée allemande.
            Ce bouquin permet de répondre à une question simple : Est-ce que les nazis étaient-ils tous pourris ?
            Bon, déjà, un petit cours d’Histoire pour les endormis du fond. La Seconde Guerre Mondiale débute avec l’invasion de la Pologne par la Wehrmacht en 1939. La France et la Grande Bretagne sont obligées de riposter tant bien que mal malgré la fatigue engendrée à la suite de la Première Guerre Mondiale.
            L’Allemagne nazie se met ensuite à marcher vers l’Ouest après s’être partagée avec l’URSS de Staline, la Pologne en lambeau. Les, Pays-Bas, la Belgique et le Luxembourg passe aux mains allemandes.
            La France décide de planquer son armée derrière la ligne Maginot, après plusieurs mois de combat et parce que ce n’est pas le sujet de ce résumé même si j’adore cette partie de l’Histoire, je me dois d’aller au plus court ! Fin du cours ! Reprise du résumé !
            Suite à la déroute française, l’occupation allemande commence. De ce fait, la France est coupée en deux, une partie libre au sud et une partie occupée au nord et à l’ouest afin de protéger la façade maritime. D’un contingent de 80000 hommes en 1941, il passe à près d’un million de soldats à la veille du débarquement de Juin 1944.
            Au début de cet ouvrage, nous suivons des soldats ayant participé à la campagne de France, qui permis à la Wehrmacht de contrôler à ce moment-là une bonne partie de l’Europe. Chaque soldat y va de sa petite lettre à son entourage. Nous constatons qu’à la fin de la campagne de France, les allemands sont heureux, à la limite de la fanfaronnade. Mais on remarque au fil de notre lecture que chaque soldat à ses préoccupations propres et mis à part quelques fanatiques du régime nazi qui se permettent des exactions sur le peuple français, la plupart des autres soldats respectent et tiennent en haute estime les français comme Wilhelm Brunner qui écrit que le peuple français est digne malgré sa condition actuelle.
            Plus nous avançons dans notre lecture, plus nous avançons vers la fin de la guerre et plus les soldats allemands sont nerveux. Après certaines lettres et écrits, beaucoup de soldats apparaissent aux yeux du lecteur comme étant des êtres humains capable de ressentir des émotions.
            Chez beaucoup d’hommes nous pouvons ressentir la peur de mourir, l’envie de revoir leur fiancée ou leur famille et d’autres hommes, qui fanatisés par le 3ème Reich et une croyance en la victoire se déchaînent dans la haine du juif, de l’Homme, tout simplement.
            Cet ouvrage permet de pouvoir avoir un œil critique, sur cette période de l’Histoire et une part de vérité. Je n’en dirais pas plus à propos de ce livre. 
            Cela sera à vous de le découvrir et de répondre vous-même à la question posée au début de ce texte ! Pour ma part la réponse est claire et nette ! 
